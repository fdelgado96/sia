package ar.edu.itba.sia.g4.genetics.config;

public class Items {
    private String gauntlets;
    private String helmets;
    private String boots;
    private String weapons;
    private String chestplates;

    public String getGauntlets() {
        return gauntlets;
    }

    public Items setGauntlets(String gauntlets) {
        this.gauntlets = gauntlets;
        return this;
    }

    public String getHelmets() {
        return helmets;
    }

    public Items setHelmets(String helmets) {
        this.helmets = helmets;
        return this;
    }

    public String getBoots() {
        return boots;
    }

    public Items setBoots(String boots) {
        this.boots = boots;
        return this;
    }

    public String getWeapons() {
        return weapons;
    }

    public Items setWeapons(String weapons) {
        this.weapons = weapons;
        return this;
    }

    public String getChestplates() {
        return chestplates;
    }

    public Items setChestplates(String chestplates) {
        this.chestplates = chestplates;
        return this;
    }
}
